    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:  
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/bd_nomina", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from templeados where codigoE = ?");
            pst.setString(1, jTextField1.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txt_nombre.setText(rs.getString("nombreE"));
                txt_apellido.setText(rs.getString("apellidoE"));
                txt_puesto.setText(rs.getString("puestoE"));
                txt_sueldo.setText(rs.getString("sueldoE"));
                txt_estado.setText(rs.getString("estadoE"));
                cod=jTextField1.getText();
            } else {
                JOptionPane.showMessageDialog(null, "Cliente no registrado.");
            jTextField1.setText("");
            }
            
        }catch (Exception e){
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/bd_nomina", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into tCheques values(?,?,?,?,?,?,?,?)");
            pst.setString(1, "0");
            pst.setString(2,txt_extraordinario.getText().trim());
            pst.setString(3, txt_bonificacion.getText().trim());
            pst.setString(4, txt_comision.getText().trim());
            pst.setString(5, txt_otros.getText().trim());
            pst.setString(6, txt_anticipos.getText().trim());
            pst.setString(7, txt_descuentos.getText().trim());
            pst.setString(8, txt_total.getText().trim());
            pst.executeUpdate();
            
            txt_extraordinario.setText("");
            txt_bonificacion.setText("");
            txt_comision.setText("");
            txt_otros.setText("");
            txt_anticipos.setText("");
            txt_descuentos.setText("");
            txt_total.setText("");

        }catch (Exception e){
            System.out.print(e.getMessage());
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed

        float sueldo, sueldoex, bonificacion, comision, otro, anticipo, descuento, total;
        
        sueldo= Integer.parseInt(txt_sueldo.getText());
        sueldoex= Integer.parseInt(txt_extraordinario.getText());
        bonificacion= Integer.parseInt(txt_bonificacion.getText());
        comision= Integer.parseInt(txt_comision.getText());
        otro= Integer.parseInt(txt_otros.getText());
        anticipo= Integer.parseInt(txt_anticipos.getText());
        descuento= Integer.parseInt(txt_descuentos.getText());
        
        total= sueldo + sueldoex + bonificacion + comision + otro - anticipo - descuento;
        
        txt_total.setText(String.valueOf(total));
        
    }//GEN-LAST:event_btnTotalActionPerformed


